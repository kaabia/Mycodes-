/**
* @brief  Get the PRESS value
* @param  None.
* @retval PRESS value
*/
float BSP_PRESS_GetValue(void)
{
  uint32_t adcval;
  float retval;
  adcval = DRV_ADCPRESS_GetVal();
  retval=0.733*adcval;
  return (retval);
}

